def term(key: str, label: str | None = None) -> str:
    """返回带悬浮解释的 HTML 片段。label 可覆盖显示文字（默认用 key）。"""
    text = GLOSSARY.get(key, "")
    show = label or key
    safe = text.replace('"', "&quot;")
    return f'<span class="qterm" data-tip="{safe}">{show}</span>'


